    ap=argparse.ArgumentParser(description='Run Phase 3B executable benchmark-suite raw-result generation.')
    ap.add_argument('--systems', nargs='*', default=None, help='Benchmark systems to run; default reads benchmark_metadata.csv')
    ap.add_argument('--seeds', default='configs/seeds_30.txt')
    args=ap.parse_args()
    seed_values=[int(x.strip()) for x in (ROOT/args.seeds).read_text().splitlines() if x.strip()]
    if args.systems:
        systems=args.systems
    else:
        meta=pd.read_csv(ROOT/'benchmarks'/'benchmark_metadata.csv')
        systems=list(meta['system'].astype(str))
    raw=ROOT/'raw_results'; proc=ROOT/'processed_results'; figs=ROOT/'figures'
    raw.mkdir(exist_ok=True); proc.mkdir(exist_ok=True); figs.mkdir(exist_ok=True)
    all_runs=[]; all_gen=[]; all_runtime=[]; all_mq=[]; all_dtmc=[]; all_ablation=[]; all_sens=[]; all_assign=[]
